    private void llenarProducto(){
        try {
            int cmb = cmbTipo.getSelectedIndex();
            if (cmb == 0) {
                throw new Exception("Rellena los campos");
            }
            JTextComponent[] textos = (cmb == 1) ? new JTextComponent[] {tfNombre, tfColor, tfPrecio, jTextArea1} :
                    new JTextComponent[] {tfNombre, tfColor, tfPrecio, jTextArea1, tfTalla} ;
            for (JTextComponent c : textos) {
                if (c.getText().equals("")) {
                    throw new Exception("Rellena los campos");
                }
            }
            int i = 0;
            producto.tipo = cmbTipo.getSelectedItem().toString();
            producto.nombre = textos[i++].getText();
            producto.color = textos[i++].getText();
            producto.precio = Float.parseFloat(textos[i++].getText());
            producto.descripcion = textos[i++].getText();
            if (cmb == 2)
                producto.talla = textos[i++].getText();
        } catch (NumberFormatException ex){
            showMessageDialog(this, "Por favor, escribe el precio correctamente");
        } catch (Exception ex) {
            showMessageDialog(this, ex.getMessage());
        }
    }

