    private void jImageBox1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jImageBox1MouseClicked
        try {
            JFileChooser j = new JFileChooser();
            FileNameExtensionFilter fil = new FileNameExtensionFilter("JPG, PNG & GIF", "jpg", "png", "gif");
            j.setFileFilter(fil);
            String ruta = "";
            if (j.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
                ruta = j.getSelectedFile().getAbsolutePath();
                tfFotoRuta.setText(ruta);
            }
            File archivo = new File(ruta);
            producto.Foto = new byte[(int) archivo.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(producto.Foto);
            BufferedImage image = null;
            InputStream in = new ByteArrayInputStream(producto.Foto);
            image = ImageIO.read(in);
            jImageBox1.setIcon(new ImageIcon(image));
            jImageBox1.setText("");
        } catch (HeadlessException | IOException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jImageBox1MouseClicked

